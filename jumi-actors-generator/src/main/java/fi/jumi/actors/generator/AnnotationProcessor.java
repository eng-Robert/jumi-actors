package fi.jumi.actors.generator;

import javax.annotation.processing.*;
import javax.lang.model.SourceVersion;
import javax.lang.model.element.TypeElement;
import javax.tools.Diagnostic;
import java.util.Set;

@SupportedAnnotationTypes("fi.jumi.actors.generator.GenerateEventizer")
public class AnnotationProcessor extends AbstractProcessor {

    @Override
    public synchronized void init(ProcessingEnvironment processingEnv) {
        super.init(processingEnv);
        processingEnv.getMessager().printMessage(Diagnostic.Kind.NOTE, "init");
    }

    @Override
    public boolean process(Set<? extends TypeElement> annotations, RoundEnvironment roundEnv) {
        processingEnv.getMessager().printMessage(Diagnostic.Kind.OTHER, "process");
        return false;
    }

    @Override
    public SourceVersion getSupportedSourceVersion() {
        // avoid warnings on newer JVMs, even though we compile on JDK 6
        return processingEnv.getSourceVersion();
    }
}
